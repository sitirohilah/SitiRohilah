package com.example.acara15;

public class herolists {
    private final Integer medsos;
    private final String name;

    public herolists(Integer medsos, String name) {
        this.medsos = medsos;
        this.name = name;
    }

    public Integer getMedsos() {
        return medsos;
    }

    public String getName() {
        return name;
    }
}
